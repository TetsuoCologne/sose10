#include "stdafx.h"
#include <iostream>
#include <string>

using namespace std;

int _tmain(int argc, _TCHAR* argv[])
{
	int n;
	n = -12;
	cout << "n = " << n << endl;
	unsigned int ui;
	ui = -1;
	cout <<"ui = " << ui << endl;
	short s;
	s = 123456;
	cout << "s = " << s << endl;
	long l;
	l = -12;
	cout << "l = " << l << endl;
	unsigned long ul;
	ul = 12;
	cout << "ul = " << ul << endl;
	char c;
	c = 12;
	cout << "c = " << c << endl;
	float f;
	f = 12.2;
	cout << "f = " << f << endl;
	double d;
	d = 12.2;
	cout << "d = " << d << endl;
	string str;
	str = 12;
	cout << "str = " << str << endl;
	bool b;
	b = -12;
	cout << "b = " << b << endl;
	return 0;
}

